package com.javaex.ex01;

public class Ex10 {
	public static void main(String[] args){
		
		 int a = 7;
		 int b = 2;
		 
		 int sum = a + b;
		 
		 System.out.println(sum);
		 System.out.println(a + b);
		 System.out.println(a - b);
		 System.out.println(a * b);
		 System.out.println(a / b);
		 System.out.println(a % b);
		 
		 System.out.println("------------------------------");
		 
		 int var = -3;
		 int pVar = +var;
		 int mVar = -var;
		 
		 System.out.println(pVar);
		 System.out.println(mVar);
		 
		 System.out.println("-------------------------------");
		 
		 //증감 연산자
		 System.out.println(a);
		 System.out.println(++a);
		 System.out.println(a);
		 
		 System.out.println(b);
		 System.out.println(++b);
		 System.out.println(b);
		 
		 System.out.println("------------------------------");
		 
		 System.out.println(a);
		 System.out.println(a++);
		 System.out.println(a);
	}

}
